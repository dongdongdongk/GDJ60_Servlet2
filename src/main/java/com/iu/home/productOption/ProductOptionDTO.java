package com.iu.home.productOption;

public class ProductOptionDTO {
	private int optionNum;
	private Long productNum;
	private String optionName;
	private int optionPrise;
	private int optionjago;
	public int getOptionNum() {
		return optionNum;
	}
	public void setOptionNum(int optionNum) {
		this.optionNum = optionNum;
	}
	public Long getProductNum() {
		return productNum;
	}
	public void setProductNum(Long productNum) {
		this.productNum = productNum;
	}
	public String getOptionName() {
		return optionName;
	}
	public void setOptionName(String optionName) {
		this.optionName = optionName;
	}
	public int getOptionPrise() {
		return optionPrise;
	}
	public void setOptionPrise(int optionPrise) {
		this.optionPrise = optionPrise;
	}
	public int getOptionjago() {
		return optionjago;
	}
	public void setOptionjago(int optionjago) {
		this.optionjago = optionjago;
	}
}
